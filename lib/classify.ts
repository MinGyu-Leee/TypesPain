function scorePatterns(text: string): Map<SufferingKey, number> {
  const scores = new Map<SufferingKey, number>();
  for (const { key, re, score } of SUFFERING_PATTERNS) {
    if (re.test(text)) {
      scores.set(key, (scores.get(key) ?? 0) + score);
    }
  }
  return scores;
}

