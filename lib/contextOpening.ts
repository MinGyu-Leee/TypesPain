// 팔고별 generic fallback (패턴 미매칭 시)
const GENERIC_OPENINGS: Record<SufferingKey, string> = {
  생고:    '살아있다는 것 자체의 무게가 느껴지는군요.',
  노고:    '몸과 마음이 예전 같지 않다는 변화가 느껴지는군요.',
  병고:    '몸이 아프다는 게 마음에도 무겁게 느껴지는군요.',
  사고:    '죽음이 이렇게 가깝게 느껴지는 순간이군요.',
  애별리고: '소중한 사람과의 거리가 느껴질 때의 그 그리움이 있군요.',
  원증회고: '피하고 싶어도 피할 수 없는 상황의 무게가 느껴지는군요.',
  구부득고: '원하는 것에 닿지 못할 때의 그 답답함이 있군요.',
  오온성고: '마음이 뭔지도 모르게 힘든 그 상태가 느껴지는군요.',
};

function generateContextOpening(question: string, sufferingKey: SufferingKey): string {
  for (const { re, build } of SITUATION_PATTERNS) {
    const m = question.match(re);
    if (m) return build(m);
  }
  return GENERIC_OPENINGS[sufferingKey] ?? '';
}

export function buildContextualParagraph(
  question: string,
  sufferingKey: SufferingKey,
  fixedParagraph: string,
): string {
  const opening = generateContextOpening(question, sufferingKey);
  if (!opening) return fixedParagraph;

  // 고정 단락의 첫 문장을 동적 도입부로 교체
  // 첫 문장 끝: [.!?。] 다음에 공백이 오는 지점
  const m = fixedParagraph.match(/^([\s\S]+?[.!?。])\s+([\s\S]+)$/);
  if (m) {
    return opening + ' ' + m[2]; // 첫 문장 제거, 도입부 + 나머지
  }

  // 문장 경계를 못 찾으면 앞에 붙임
  return opening + ' ' + fixedParagraph;
}
